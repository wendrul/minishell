#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *s)
{
	char	*dup;

	dup = (char *)malloc((ft_strlen(s) + 1) * sizeof(*dup));
	if (!dup)
		return (NULL);
	ft_memmove(dup, s, ft_strlen(s) + 1);
	return (dup);
}
